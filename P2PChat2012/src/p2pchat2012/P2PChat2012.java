    private void Start_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Start_buttonActionPerformed
        // TODO add your handling code here:
        boolean flag=false;
        if (ID_name.getText().equals("")){ 
            JOptionPane.showMessageDialog(rootPane, "Please fill out your ID!", "Error", JOptionPane.ERROR_MESSAGE);
            flag=true;
        }
        if (Port_use.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please fill out your Port!", "Error", JOptionPane.ERROR_MESSAGE);
            flag=true;
        }
        if (!flag){
            try {
                Manager manager= new Manager(ID_name.getText(),Port_use.getText());
                manager.setVisible(true);
                this.dispose();
            } catch (UnknownHostException ex) {
                Logger.getLogger(P2PChat2012.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_Start_buttonActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        final JFileChooser chooser = new JFileChooser() {

            public void approveSelection() {
                if (getSelectedFile().isFile()) {
                    return;
                } else {
                    super.approveSelection();
                }
            }
        };
        chooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        chooser.showDialog(ID_name, "Select");
        jTextField1.setText(chooser.getSelectedFile().toString() + '\\');


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // ok, save directory
        readwritefile rw_file = new readwritefile();
        basedir = jTextField1.getText();
        if (check_ask.isSelected()) {
            choosefile = true;
            readwritefile.writefile(basedir,"true");
        } else {
            choosefile = false;
            readwritefile.writefile(basedir,"false");
        }         
        setting_frame.setVisible(false);
        
    }//GEN-LAST:event_jButton2ActionPerformed

